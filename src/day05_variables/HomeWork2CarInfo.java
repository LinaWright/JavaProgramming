package day05_variables;

public class HomeWork2CarInfo {
    public static void main(String[] args) {
        String carModel, driverName;
        int licenseNumber, speed;
        boolean isAutomatic;
        char licenseClass;

        carModel = "289XL";
        driverName = "lina";
        licenseNumber = 468975;
        speed = 280;
        isAutomatic = true;
        licenseClass = 'E';

        System.out.println("carModel = " + carModel);
        System.out.println("driverName = " + driverName);
        System.out.println("licenseNumber = " + licenseNumber);
        System.out.println("speed = " + speed);
        System.out.println("licenseClass = " + licenseClass);
        System.out.println("isAutomatic = " + isAutomatic);
    }
}
