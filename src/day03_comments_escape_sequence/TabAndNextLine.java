package day03_comments_escape_sequence;

public class TabAndNextLine
{
    public static void main(String[] args) {
        System.out.println("My name starts with 'S'");
        System.out.println("This is not tabbed");
        System.out.println("\tThis is tabbed once");
        System.out.println("\t\tThis is tabbed twice");
        System.out.println("    This is tabbed with spaces");
        //examples of next line
        System.out.println("What's your name?\nMy name is Lina");
    }
}
